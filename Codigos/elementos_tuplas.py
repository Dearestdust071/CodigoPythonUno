# Definir una tupla con nombres de países
countries_tuple = ("Mexico", "Brasil", "Argentina", "España")

# Imprimir la tupla completa
print(countries_tuple, "\n")

# Acceder al segundo elemento de la tupla (indice 1)
print(f"Posicion 1: {countries_tuple[1]}")  # 'Brasil'

# Otro ejemplo del uso de indices
print(f"Posicion 3: {countries_tuple[3]}")  # 'España'

# Acceder al ultimo elemento de la tupla usando indice negativo
print(f"Posicion -1: {countries_tuple[-1]}")  # 'España'

# Acceder al primer elemento de la tupla usando índice negativo
print(f"Posicion -4: {countries_tuple[-4]}")  # 'Mexico'