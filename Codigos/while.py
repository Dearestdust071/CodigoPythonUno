# Primer ejemplo con un bucle 'while' que imprime los números menores a 3
x = 1

# Mientras x sea menor que 3, ejecutará el bloque de código dentro del while
while x < 3:
    print(x)  # Imprime el valor de x
    x += 1  # Incrementa el valor de x en 1 en cada iteración

# Al terminar el bucle, se imprime "Fin."
print("Fin.")

# Segundo ejemplo con un bucle 'while' que imprime "Ernesto" 9 veces
x = 1

# Mientras x sea menor que 10, ejecutará el bloque de código dentro del while
while x < 10:
    print("Ernesto")  # Imprime la palabra "Ernesto"
    x += 1  # Incrementa el valor de x en 1 en cada iteración